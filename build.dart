import 'dart:io';

import 'package:gg_console_colors/gg_console_colors.dart';

// .............................................................................
const dartEntry = 'lib/src/main.dart';
const generatedFolder = 'typescript/generated';
const jsName = 'bridge-js';
const wasmName = 'bridge-wasm';

bool debug = false;
bool clean = false;

// .............................................................................
Future<void> main(List<String> args) async {
  debug = args.contains('--debug') || args.contains('-d');
  clean = args.contains('--clean');

  if (clean) {
    await _clean();
  }

  _ensurePrerequisites();
  _ensureGeneratedFolder();

  await _compileToJs();
  await _wrapJsIntoTypescript();

  await _compileToWasm();
  await _wrapWasmIntoTypescript();

  print(green('Build complete.'));
}

// .............................................................................
String get _jsBase => '$generatedFolder/$jsName';
String get _wasmBase => '$generatedFolder/$wasmName';

// .............................................................................
void _ensurePrerequisites() {
  if (!File(dartEntry).existsSync()) {
    stderr.writeln(red('Missing Dart entry point: $dartEntry'));
    exit(2);
  }
}

void _ensureGeneratedFolder() {
  final dir = Directory(generatedFolder);
  if (!dir.existsSync()) {
    dir.createSync(recursive: true);
  }
}

Future<void> _clean() async {
  final dir = Directory(generatedFolder);
  if (dir.existsSync()) {
    print(darkGray('Cleaning $generatedFolder ...'));
    await dir.delete(recursive: true);
  }
}

// .............................................................................
Future<void> _compileToJs() async {
  final out = '$_jsBase.js';
  await _run('Compiling $dartEntry → $out', [
    'dart',
    'compile',
    'js',
    dartEntry,
    '-o',
    out,
    debug ? '-O0' : '-O4',
    debug ? '--source-maps' : '--no-source-maps',
  ]);
}

Future<void> _wrapJsIntoTypescript() async {
  final jsFile = '$_jsBase.js';
  final tsFile = '$_jsBase.ts';

  print(darkGray('Wrapping $jsFile → $tsFile'));

  final body = await File(jsFile).readAsString();

  final wrapped = <String>[
    '// Generated by build.dart. Do not edit.',
    '// @ts-nocheck',
    '/* eslint-disable */',
    '',
    'export function bridgeJs(): void {',
    body,
    '}',
    '',
  ].join('\n');

  await File(tsFile).writeAsString(wrapped);

  await File(jsFile).delete();
  final deps = File('$jsFile.deps');
  if (deps.existsSync()) await deps.delete();
}

// .............................................................................
Future<void> _compileToWasm() async {
  final out = '$_wasmBase.wasm';
  await _run('Compiling $dartEntry → $out', [
    'dart',
    'compile',
    'wasm',
    dartEntry,
    '-o',
    out,
    debug ? '-O0' : '-O4',
    debug ? '--source-maps' : '--no-source-maps',
  ]);
}

Future<void> _wrapWasmIntoTypescript() async {
  final mjsFile = '$_wasmBase.mjs';
  final tsFile = '$_wasmBase.ts';

  print(darkGray('Wrapping $mjsFile → $tsFile'));

  final body = await File(mjsFile).readAsString();
  final wrapped = <String>[
    '// Generated by build.dart. Do not edit.',
    '// @ts-nocheck',
    '/* eslint-disable */',
    '',
    body,
    '',
  ].join('\n');

  await File(tsFile).writeAsString(wrapped);
  await File(mjsFile).delete();
}

// .............................................................................
Future<void> _run(String description, List<String> command) async {
  print(yellow(description));
  final p = await Process.start(
    command.first,
    command.sublist(1),
    mode: ProcessStartMode.inheritStdio,
  );
  final code = await p.exitCode;
  if (code != 0) {
    stderr.writeln(red('Command failed (exit $code): ${command.join(' ')}'));
    exit(code);
  }
}
